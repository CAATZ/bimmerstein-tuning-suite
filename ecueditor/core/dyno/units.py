from __future__ import annotations

def f_to_c(f: float) -> float: return (f - 32.0) * 5.0 / 9.0
def c_to_k(c: float) -> float: return c + 273.15
def lb_to_kg(lb: float) -> float: return lb * 0.45359237
def ft_to_m(ft: float) -> float: return ft * 0.3048
def mph_to_ms(mph: float) -> float: return mph * 0.44704
